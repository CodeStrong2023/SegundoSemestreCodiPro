# Operadores de comparacion: son para saber si dos valores son iguales o diferentes
d = 4
b = 2 # con 4 es True y con otro valor es False
resultado = d == b # comprobamos si son iguales
""""
# d == b es una expresion, primero se evalua el lado der de la expresion 
o entre parentesis tiene prioridad el parentesis
"""
print(resultado)

#Operador diferente
resultado = d != b
print(resultado)

#Operador Mayor que
resultado = d > b
print(resultado)

#Operador Menor que
resultado = d < b
print(resultado)

#Operador Menor o igual que
resultado = d <= b
print(resultado)

#Operador Mayor o igual que
resultado = d >= b
print(resultado)