miVariable3 = 10
print(miVariable3)

# Operadores de reasignacion
miVariable3 = miVariable3 +1
print(miVariable3)

# Sintasis reducida del incremento con reasignacion anterior
miVariable3 += 1
print(miVariable3)

# miVariable3 = miVariable3 - 2
miVariable3 -= 2
print(miVariable3)

# miVariable3 = miVariable3 * 3
miVariable3 *= 3
print(miVariable3)

# miVariable3 = miVariable3 / 2
miVariable3 /= 2
print(miVariable3)

# miVariable3 = miVariable3 % 2
miVariable3 %= 2
print(miVariable3)

# Operadores que sirven para cadenas de texto
# + concatenacion
hola = "ho" + "la"
print(hola)

# * repeticion
saludo = "hello world " * 4
print(saludo)

